#!/usr/bin/python3

print("Imprimir a tabela verdade AND")
print("Valor Verdadeiro E valor verdadeiro={0}".format(True and True))
print("Valor Verdadeiro E valor False={0}".format(True and False))
print("Valor Falso E valor Verdadeiro={0}".format(False and True))
print("Valor Falso E valor Falso={0}".format(False and False))

print('\n')

print("Imprimir a tabela verdade OR")
print("Valor Verdadeiro OU valor verdadeiro={0}".format(True or True))
print("Valor Verdadeiro OU valor False={0}".format(True or False))
print("Valor Falso OU valor Verdadeiro={0}".format(False or True))
print("Valor Falso OU valor Falso={0}".format(False or False))

print('\n')

print("Imprimir a tabela verdade XOR")
print("Valor Verdadeiro XOR valor verdadeiro={0}".format(True ^ True))
print("Valor Verdadeiro XOR valor False={0}".format(True ^ False))
print("Valor Falso XOR valor Verdadeiro={0}".format(False ^ True))
print("Valor Falso XOR valor Falso={0}".format(False ^ False))
